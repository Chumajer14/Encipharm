export function normalizeLanguage(language) {
  const raw = String(language || "es").trim().toLowerCase();
  if (["en", "en-us", "en_us", "english", "ingles", "inglés"].includes(raw)) return "en";
  if (["pt", "pt-br", "pt_br", "portugues", "portugués", "portuguese"].includes(raw)) return "pt";
  return "es";
}

function normalizeFromTranslatedLanguage(trimmed) {
  for (const dictionary of [DICTIONARY.en, DICTIONARY.pt]) {
    const sourceEntry = Object.entries(dictionary).find(([, translated]) => translated === trimmed);
    if (sourceEntry) return sourceEntry[0];
  }
  return trimmed;
}

function translateValue(value, language) {
  const normalizedLanguage = normalizeLanguage(language);
  const dictionary = DICTIONARY[normalizedLanguage] || {};
  const raw = String(value || "");
  const trimmed = raw.trim();
  if (!trimmed) return raw;

  const sourceText = normalizeFromTranslatedLanguage(trimmed);
  if (normalizedLanguage === "es") {
    if (sourceText !== trimmed) return raw.replace(trimmed, sourceText);
    return raw;
  }

  const translated = dictionary[sourceText] || dictionary[trimmed];
  if (translated) return raw.replace(trimmed, translated);
  return raw;
}

export function translateText(value, language) {
  return translateValue(value, language);
}

export function translateStaticDom(language) {
  const normalizedLanguage = normalizeLanguage(language);
  if (!normalizedLanguage || typeof document === "undefined" || !document.body) return;
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  const textNodes = [];
  while (walker.nextNode()) {
    const node = walker.currentNode;
    const parent = node.parentElement;
    if (!parent || ["SCRIPT", "STYLE", "TEXTAREA"].includes(parent.tagName)) continue;
    textNodes.push(node);
  }
  textNodes.forEach((node) => {
    const translated = translateValue(node.nodeValue, normalizedLanguage);
    if (translated !== node.nodeValue) node.nodeValue = translated;
  });
  document.querySelectorAll("[placeholder], [title], [aria-label]").forEach((element) => {
    ["placeholder", "title", "aria-label"].forEach((attribute) => {
      const value = element.getAttribute(attribute);
      if (!value) return;
      const translated = translateValue(value, normalizedLanguage);
      if (translated !== value) element.setAttribute(attribute, translated);
    });
  });
}

export function useI18n() {
  const { backendUser } = useAuth();
  const language = normalizeLanguage(backendUser?.language);
  const dictionary = DICTIONARY[language] || {};

  return {
    language,
    t: (key) => translateValue(dictionary[key] ? key : key, language),
  };
}
